    private void btnXoa_KhachHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoa_KhachHangActionPerformed
        int row = tblKhachHang_KhachHang.getSelectedRow();
        if (row == -1) {
            ThongBao("Mời bạn chọn vào bảng", "Lỗi", 2);
            return;
        }

        try {
            KhachHangModel kh = new KhachHangModel();
            khachHangService = new KhachHangService();
            hoaDonService = new HoaDonService();
            kh.setMaKhachHang(Integer.parseInt(txtMaKhachHang_KhachHang.getText()));
            List<HoaDonModel> hd = hoaDonService.findAllByMaKhachHang(kh.getMaKhachHang());
            int soPhieuMua = hd.get(0).getSoPhieuMua();
            if (soPhieuMua != 0) {
                ThongBao("khách hàng đà mua " + soPhieuMua + " hóa đơn nên bạn không thể xóa", "Lỗi", 2);
                return;
            }

            khachHangService.edit(kh, kh.getMaKhachHang(), 2);

            ThongBao("Xóa Thành Công", "Thành công", 3);
            clearKhachHang();
            renderKhachHang();
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnXoa_KhachHangActionPerformed

    private void btnSua_KhachHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSua_KhachHangActionPerformed
        int row = tblKhachHang_KhachHang.getSelectedRow();
        if (row == -1) {
            ThongBao("Mởi bại chọn vào bảng", "Lỗi", 2);
            return;
        }
        if (txtTenKhachHang_KhachHang.getText().equals("") || txtDiaChi_KhachHang.getText().equals("") || txtSDT_KhachHang.getText().equals("")) {
            ThongBao("Bạn nhập bị thiếu", "Lỗi", 2);
            return;
        }
        if (txtSDT_KhachHang.getText().matches("^\\d{10}$") == false) {
            ThongBao("Số điện thoại phải 10 số còn của bạn là" + txtSDT_KhachHang.getText().trim().length() + " số", "Lỗi", 2);
            return;
        }
        try {
            KhachHangModel kh = new KhachHangModel();
            khachHangService = new KhachHangService();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(txtNgaySinh_KhachHang.getDate());
            kh.setMaKhachHang(Integer.parseInt(txtMaKhachHang_KhachHang.getText()));
            kh.setTenKhachHang(txtTenKhachHang_KhachHang.getText());
            if (rbtnNam_KhachHang.isSelected()) {
                kh.setGioiTinh(1);
            } else {
                kh.setGioiTinh(0);
            }
            kh.setNgaySinh(date);
            kh.setDiaChi(txtDiaChi_KhachHang.getText());
            kh.setSdt(txtSDT_KhachHang.getText());
            kh.setGhiChu(txtGhiChu_KhachHang.getText());
            kh.setMaLoaiKhachHang(Integer.parseInt(GetCbbSelected(cbbLoaiKhachHang_KhachHang.getSelectedItem(), (ArrayList<displayvalueModel>) loaiKhach_model)));
            khachHangService.edit(kh, kh.getMaKhachHang(), 1);

            ThongBao("Sửa Thành Công", "Thành công", 3);
            clearKhachHang();
            renderKhachHang();
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnSua_KhachHangActionPerformed

