    void renderKhachHang() {
        khachHangService = new KhachHangService();

        model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblKhachHang_KhachHang.setModel(model);
        model.addColumn("Stt");
        model.addColumn("Mã Khách Hàng");
        model.addColumn("Tên Khách hàng");
        model.addColumn("Ngày Sinh");
        model.addColumn("Giới Tính");
        model.addColumn("Địa Chỉ");
        model.addColumn("SDT");
        model.addColumn("Loại Khách Hàng");
        model.addColumn("Ghi Chú");
        List<KhachHangModel> khachHangItem = khachHangService.findAll();

        for (int i = 0; i < khachHangItem.size(); i++) {
            KhachHangModel get = khachHangItem.get(i);
            model.addRow(new Object[]{
                (i + 1),
                get.getMaKhachHang(),
                get.getTenKhachHang(),
                get.getNgaySinh(),
                get.getGioiTinh() == 1 ? "Nam" : "Nữ",
                get.getDiaChi(),
                get.getSdt(),
                get.getLoaiKhachHang(),
                get.getGhiChu()
            });

        }

    }

    public void setSelectedCombobox(String cbbselected, JComboBox cbb) {
        for (int i = 0; i < cbb.getItemCount(); i++) {
            Object obj = cbb.getItemAt(i);
            if (obj != null) {
                if (cbbselected.trim().equals(obj)) {
                    cbb.setSelectedItem(obj);
                }
            }
        }
    }

    public String GetCbbSelected(Object cbb, ArrayList<displayvalueModel> pramater) {
        Object obj = cbb;
        int index = -1;
        for (int i = 0; i < pramater.size(); i++) {
            displayvalueModel get = pramater.get(i);
            if (get.getDisplayvalue().equals(obj)) {
                index = i;
                break;
            }
        }
        if (index == -1) {
            return null;
        }
        return pramater.get(index).toString();
    }

