    private void tblKhachHang_KhachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhachHang_KhachHangMouseClicked
        try {
            int row = tblKhachHang_KhachHang.getSelectedRow();

            txtMaKhachHang_KhachHang.setText(tblKhachHang_KhachHang.getValueAt(row, 1).toString());
            txtTenKhachHang_KhachHang.setText(tblKhachHang_KhachHang.getValueAt(row, 2).toString());
            txtDiaChi_KhachHang.setText(tblKhachHang_KhachHang.getValueAt(row, 5).toString());
            txtSDT_KhachHang.setText(tblKhachHang_KhachHang.getValueAt(row, 6).toString());
            txtGhiChu_KhachHang.setText(tblKhachHang_KhachHang.getValueAt(row, 8).toString());
            setSelectedCombobox(tblKhachHang_KhachHang.getValueAt(row, 7).toString(), cbbLoaiKhachHang_KhachHang);
            String gioitinh = tblKhachHang_KhachHang.getValueAt(row, 4).toString();

            if (gioitinh.equals("Nam")) {
                rbtnNam_KhachHang.setSelected(true);
                rbtnNu_KhachHang.setSelected(false);
            } else {
                rbtnNam_KhachHang.setSelected(false);
                rbtnNu_KhachHang.setSelected(true);
            }
            Date date;
            date = new SimpleDateFormat("yyyy-MM-dd").parse((String) tblKhachHang_KhachHang.getValueAt(row, 3).toString());
            txtNgaySinh_KhachHang.setDate(date);
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_tblKhachHang_KhachHangMouseClicked

    private void txtTimKiemSanPhamKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTimKiemSanPhamKeyPressed
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            DefaultTableModel table = (DefaultTableModel) tblKhachHang_KhachHang.getModel();
            String search = txtTimKiemSanPham.getText().toLowerCase().trim();
            TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(table);
            tblKhachHang_KhachHang.setRowSorter(tr);
            tr.setRowFilter(RowFilter.regexFilter("(?i)" + search));
        }

    }//GEN-LAST:event_txtTimKiemSanPhamKeyPressed


